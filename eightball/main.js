let gangstaRoasts = [
    ` Warning fortnight related joke:yo nmommas so fat not even ninja could carry her.`,
    `Yo moma is bea'in SOOOOOOO FAAAAAT she be use din da heeway as da slip'in slude`,
    `Yo moma bea'in dat ugly dat she be yon geving dea fruddy cuggar da nute mares`,
    `Yo moma be in thinkin dat da sea wed be da wed dat da spongebob be smokin`
]


function randRoast() {
    let n = Math.random() * gangstaRoasts.length
    n = Math.floor(n)
    return gangstaRoasts[n]
}

let roast = document.querySelector('#roast')
let question = document.querySelector('#question')

function updateRoast(event) {
    if (event.key === "Enter") {
        roast.innerText = randRoast()
        question.innerText = ""
        return false
    }
}

question.onkeypress = updateRoast


